  Widget _showForm() {
    return ModalProgressHUD(
      inAsyncCall: showspinner,
      child: new Container(
          padding: EdgeInsets.all(16.0),
          child: new Form(
            key: _formKey,
            child: new ListView(
              shrinkWrap: true,
              children: <Widget>[
                showLogo(),
                showEmailInput(),
                showPasswordInput(),
                showPrimaryButton(),
                showSecondaryButton(),
                showErrorMessage(),
                Row(
                    mainAxisAlignment: MainAxisAlignment.center,
                    children: <Widget>[
                      facebookLoginButton(),
                      SizedBox(width: 80),
                      googleLoginButton(),
                    ])
              ],
            ),
          )),
    );
  }

  void _signInwithFacebook() async {
    try {
      setState(() {
        showspinner = true;
      });
      FirebaseUser fbUser = await widget.auth.facebookSignedIn();
      print("Signed in as ${fbUser.displayName}");
      widget.loginCallback();
      setState(() {
        showspinner = false;
      });
    } catch (e) {
      print(e);
    }
  }

  Widget googleLoginButton() {
    return IconButton(
      onPressed: () {
        _signInwithGoogle();
      },
      icon: Icon(Icons.mail, color: Colors.red, size: 40),
    );
  }

  void _signInwithGoogle() async {
    try {
      setState(() {
        showspinner = true;
      });
      FirebaseUser userId = await widget.auth.googleSignedIn();
      print("SignIn  ${userId.displayName}");
      widget.loginCallback();
      setState(() {
        showspinner = false;
      });
    } catch (e) {
      print(e);
    }
  }

  Widget facebookLoginButton() {
    return IconButton(
      onPressed: _signInwithFacebook,
      icon: Icon(FontAwesomeIcons.facebook, color: Colors.blue, size: 40),
    );
  }

  Widget showErrorMessage() {
    if (_errorMessage.length > 0 && _errorMessage != null) {
      return new Text(
        _errorMessage,
        style: TextStyle(
            fontSize: 13.0,
            color: Colors.red,
            height: 1.0,
            fontWeight: FontWeight.w300),
      );
    } else {
      return new Container(
        height: 0.0,
      );
    }
  }

  Widget showLogo() {
    return new Hero(
      tag: 'hero',
